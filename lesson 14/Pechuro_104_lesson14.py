# задание 4 в текстовом файле посчитать кол-во строк и для каждой строки кол-во в ней символов
with open('task.txt', 'r') as file:
    file2 = file.readlines()  # читаем в виде списка из строк
    stroka = 0  # задаем счетчик для подсчета кол-ва строк
    print('количество строк равно', len(file2))  # количество строк
    for h in file2:  # идем циклом по каждой строке
        stroka += 1  # увеличиваем счетчик на +1, то есть на новую строку
        print(f'на {stroka} строке указано - {h} состоящее из {len(h)} символов')


