# задание 1 в файле записаны слова и числа через пробел. найти сумму всех чисел
task = open('task.txt', 'r')
x = task.readlines()  # дает наш текст в виде списка состоящего из строк
sum_ = 0  # переменная для подсчета суммы
for i in x:  # заходим циклом в список
    for g in i:  # проходимся циклом по строке
        if g.isdigit():  # если строка состоит из цифр
            sum_ += int(g)  # то суммируем их
print(sum_)
task.close()

# задание 2 файл содержит числа и буквы в отдельной строке. считать список так чтобы сначала были цифры по возраст,
# потом слова по возрастанию их длины
with open('task.txt', 'r') as file:
    file_ = file.read()  # даст наш файл в виде строк
    number = []  # пустой список для добавления чисел
    words = []  # пустой список для добавления слов
    for k in file_.split():  # заходим циклом в список
        if k.isdigit():  # если элемент цифровой
            number.append(int(k))  # добавляем его в список для чисел
        else:
            words.append(str(k))  # иначе с список для слов
    words.sort(key=len)  # сортируем список слов по их длине
    number.sort()  # сортируем список чисел по возрастанию
    total = number + words  # объединяем два списка
    print(total)

