# задание 4 число секунд отображалось в виде дни:часы:минуты:секунды
def seconds_():
    seconds = int(input('введите количество секунд: '))
    days = seconds // 86400  # переводим в дни
    seconds2 = seconds % 86400  # находим остаток от деления
    hours = seconds2 // 3600  # этот остаток переводим в часы
    seconds3 = seconds2 % 3600  # находим остаток от деления
    minuts = seconds3 // 60  # остаток переводим в минуты
    seconds4 = seconds3 % 60  # находим сколько осталось секунд
    print(f'{days}:{hours}:{minuts}:{seconds4}')


seconds_()


# задание 5 сколько гласных и согласных в строке введенной с клавиатуры
def stroka():
    stroka_ = str(input('введите строку: '))
    vowels = 0  # гласные
    consonants = 0  # согласные
    for q in stroka_:
        if q == 'e' or q == 'y' or q == 'u' or q == 'i' or q == 'o' or q == 'a':
            vowels += 1
        else:
            consonants += 1
    print(f'в слове {stroka_} имеется {vowels} гласных и {consonants} согласных букв')


stroka()


