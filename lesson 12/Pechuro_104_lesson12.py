# дз если в ф-цию передается кортеж - посчитать длину всех слов, если список - посчитать кол-во букв и чисел,
# если число- кол-во нечетных цифр, строка - кол-во букв.
def chek(g):  # функция с одним параметром
    if type(g) == tuple:  # если подается кортеже
        for h in g:  # проходимся по нему циклом
            if type(h) == str:  # если в кортеже присутствуют слова
                print(f'длина слова {h} равна {len(h)}')  # то считаем длину каждого слова
    elif type(g) == list:  # если подается список
        number = 0  # переменная для подсчета количетсва чисел в списке
        words = 0  # переменная для подсчета букв в словах из списка
        for x in g:  # проходимся циклом по списку
            if type(x) == int:  # если в списке указано число
                number += 1  # то считаем количество чисел в списке
            elif type(x) == str:  # если в списке указано слово
                for d in x:  # проходимся циклом по каждому слову
                    if d.isalpha():  # проверяем каждую букву в слове
                        words += 1  # и подсчитываем количество букв
        print(f'количетсво чисел в списке равно {number},'
              f'количество букв в словах из списка равно {words}')
    elif type(g) == int:  # если подается число
        g = str(g)  # приводим его к строке
        count = 0  # перемнная для подсчета нечетных цифр
        for c in g:  # проходимся циклом по строке
            if int(c) % 2 != 0:  # если число нечетное
                count += 1  # то +1 к счетчику
        print(f'количество нечетных чисел равно {count}')
    elif type(g) == str:  # если подается строка
        print(f'количество букв равно {len(g)}')  # считаем количество букв
    else:
        return 'вы ввели неверные данные'


chek(('i', 'live', 'in', 'minsk'))  # подаем кортеж на проверку
chek(['я', 'родилась', 23, 2, 1997])  # подаем список на проверку
chek('abracadabra')  # подаем строку на проверку
chek(123456789)  # подаем число на проверку


