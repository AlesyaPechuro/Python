# задание 2 пользователь выбирает круг, прямоугольник или треугольник. вычисляется площадь отдельной функцией
def circle(r):  # функция для нахождения площади круга
    return 3.14 * r ** 2


def rectangle(a, b):  # функция для нахождения площади прямоугольника
    return a * b


def triangle(c, h):  # функция для нахождения площади треугольника
    return c * h / 2


def square():
    choice = input('выберите круг, прямоугольник или треугольник: ')
    if choice == 'круг':
        r = int(input('введите значение радиуса круга: '))
        return circle(r)
    elif choice == 'прямоугольник':
        a, b = int(input('введите значение стороны а: ')), int(input('введите значение стороны b: '))
        return rectangle(a, b)
    elif choice == 'треугольник':
        c, h = int(input('введите значение основания: ')), int(input('введите значение высоты: '))
        return triangle(c, h)
    else:
        return 'что-то неверно'


square()


