# 4 есть массив с названием блюд на ужин. выводим "я не ем... " и закончить программу
l = ['суп', 'рис', 'кисель', 'щи', 'макароны']
dish = str(input('введите что вы не едите из списка блюд: '))
for i in l:
    if i == dish:
        print('я не ем щи')
        break
    print('отлично, вкусный ' + i)
