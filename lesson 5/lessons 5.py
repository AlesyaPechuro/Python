# задача 1 вводится строка и сивол. из строки удаляется символ и выводится на экран без него
stroka = str(input('введите строку: '))  # пользователь вводит строку
symbol = str(input('введите символ: '))  # пользователь вводит символ
new_stroka = " "  # чистая строка
for i in stroka:  # для врем.переменной в строке
    if i != symbol:  # если пер не равна символу
        new_stroka += i  # то она попадает в чистую строку
print(new_stroka)

# задача 2 введите начало, конец и шаг. выведите последовательность на экран
start = int(input('введите начало последовательности: '))
end = int(input('введите конец последовательности: '))
step = int(input('введите шаг последовательности: '))
for i in range(start, end, step):
    print(i)

