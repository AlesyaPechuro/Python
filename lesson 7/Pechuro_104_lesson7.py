# дз калькулятор
def calculator(a, b, c):  # объявляем функцию с 3 параметрами
    if c == '+':
        print(f'сумма чисел {a} и {b} равна', a + b)
    elif c == '-':
        print(f'разность чисел {a} и {b} равна', a - b)
    elif c == '*':
        print(f'произведение чисел {a} и {b} равно', a * b)
    elif c == '/':
        if b != 0:  # если делитель не ноль
            print(f'деление числа {a} и {b} равно', a / b)
        else:  # если делитель ноль
            print('на ноль делить нельзя!')


print(calculator(float(input('введите первое число: ')),
                 float(input('введите второе число: ')),
                 input('выберите операцию: + - * /')))
