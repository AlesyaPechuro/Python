# задание 4 аргумент - номер месяца и возвращает к какой поре года относится этот месяц
def season(m):
    if 3 <= m <= 5:
        return 'весна'
    elif 6 <= m <= 8:
        return 'лето'
    elif 9 <= m <= 11:
        return 'осень'
    elif m == 12 or 1 <= m <= 2:
        return 'зима'
    else:
        return 'бывает только 12 месяцев!'


print(season(int(input('введите номер месяца: '))))


# задание 5. аргумент - число от 0 до 1000. тру - если оно простое
def is_prime(n):
    d = 2
    while n % d != 0:
        d += 1
    return d == n


print(is_prime(int(input('введите число от 0 до 1000: '))))

