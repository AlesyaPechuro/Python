import random

# задание 4 пользователь вводит 2 числа с клавы. выводим на экран отрицательные числа между ними
klava1 = int(input('введите первое число: '))
klava2 = int(input('введите второе число: '))
while klava1 < klava2:  # пока первое число меньше второго
    klava1 += 1  # цикл проходится по каждому числу от заданного первого
    if klava1 == 0:  # как только заданное первое число доходит до нуля
        break  # цикл прекращается
    print(klava1)  # выводит все числа которые меньше второго заданного и меньше нуля

# задание 6 калькулятор
kal1 = float(input('введите первое число: '))
kal2 = float(input('введите второе число: '))
oper = input('выберите операцию: + - * /')
while True:  # запускаем цикл
    if oper == '+':
        print(f'сумма чисел {kal1} и {kal2} равна', kal1 + kal2)
    elif oper == '-':
        print(f'разность чисел {kal1} и {kal2} равна', kal1 - kal2)
    elif oper == '*':
        print(f'произведение чисел {kal1} и {kal2} равно', kal1 * kal2)
    elif oper == '/':
        if kal2 != 0:  # если делитель не ноль
            print(f'деление числа {kal1} на {kal2} равно', kal1 / kal2)
        else:  # если делитель ноль
            print('на ноль делить нельзя!')
    break  # чтобы цикл не был бесконечным

