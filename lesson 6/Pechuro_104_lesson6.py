# массив из 7 чисел. если четных больше чем нечетных то найти сумму всех цифр. если наоборот то произведение 1, 3 и 6 эл
arr = [5, 8, 10, 14, 15, 21, 24]
arr_ch = []  # создаем пустой массив для четных чисел
arr_nech = []  # создаем пустой массив для нечетных чисел
sum = 0  # переменная для нахождения суммы
prois = arr[1] * arr[3] * arr[6]  # произведение 1, 3 и 6 элементов
for i in arr:
    if i % 2 == 0:  # находим является ли число четным
        arr_ch.append(i)  # если четное то добавляем в пустой массив для четных чисел
    else:
        arr_nech.append(i)  # иначе добавялем число в пустой массив дл нечетных чисел
if len(arr_ch) > len(arr_nech):  # если количество элеметов в четном массиве больше чем в нечетном массиве
    for g in arr:
        sum += g
    print(f'сумма всех чисел из массива {arr} = {sum}')  # находим сумму всех чисел изначального массива
else:
    print(f'произведение 1, 3 и 6 элемента из массива {arr} = {prois}')  # иначе указываем произведение

